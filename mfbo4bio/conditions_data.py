def get_cell_params(clone_distribution="alpha"):

    if clone_distribution == "alpha":
        process_parameters = process_parameters_alpha
    elif clone_distribution == "beta":
        process_parameters = process_parameters_beta
    elif clone_distribution == "test":
        process_parameters = process_parameters_beta
    else:
        raise NameError(
            f"No clone distribution named {clone_distribution}. \
            Please specify 'alpha' or 'beta' as input"
        )

    data = []
    for cell_type, params in process_parameters.items():
        entry = {"cell_type": cell_type, **params}
        data.append(entry)

    df = pd.DataFrame(data)

    return df
